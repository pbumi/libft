#include "libft.h"

size_t	x_isspace(char **argv, int z)
{
	size_t	i;

	i = 0;
	while (argv[z][i] && (argv[z][i] == 31 || argv[z][i] == 32
		|| (argv[z][i] >= 9 && argv[z][i] <= 13)))
		i++;
	return (i);
}
